package animais;

public class Animais {

    private String nome;
    private String brinco;
    private int literDay;
    private int kg;
    private int literMonth;
    private String dataNasc;

    public Animais() {
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getBrinco() {
        return brinco;
    }

    public void setBrinco(String brinco) {
        this.brinco = brinco;
    }

    public int getLiterDay() {
        return literDay;
    }

    public void setLiterDay(int literDay) {
        this.literDay = literDay;
    }

    public int getKg() {
        return kg;
    }

    public void setKg(int kg) {
        this.kg = kg;
    }

    public int getLiterMonth() {
        return literMonth;
    }

    public void setLiterMonth(int literMonth) {
        this.literMonth = literMonth;
    }

    public String getDataNasc() {
        return dataNasc;
    }

    public void setDataNasc(String dataNasc) {
        this.dataNasc = dataNasc;
    }


}
